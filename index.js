function useInterval(callback, delay = 30000) {
    const savedCallback = react.useRef();
    // Remember the latest callback.
    react.useEffect(() => {
        savedCallback.current = callback;
    }, [callback]);
    // Set up the interval.
    react.useEffect(() => {
        function tick() {
            if (savedCallback.current)
                savedCallback.current();
        }
        if (delay !== null) {
            const id = setInterval(tick, delay);
            return () => clearInterval(id);
        }
    }, [delay]);
}

function generateViewModelFromResult(result) {
    const channels = result?.channels || [];
    channels.sort((a, b) => {
        if (a.name < b.name) {
            return -1;
        }
        if (a.name > b.name) {
            return 1;
        }
        return 0;
    });
    return {
        channels: channels,
        total: channels.length,
        off: channels.filter((c) => c.status === Status.UNKNOWN).length,
        down: channels.filter((c) => c.status === Status.DOWN).length,
        healthy: channels.filter((c) => c.status === Status.UP).length,
        degraded: channels.filter((c) => c.status === Status.WARN).length,
        channelsToShow: channels.filter((c) => (c.status === Status.WARN || c.status === Status.DOWN))
    };
}
